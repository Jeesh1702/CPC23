class Solution
{
    public:
    Node* findIntersection(Node* head1, Node* head2)
    {
        // code goes here.
        Node *c1 = head1,*c2 = head2,*c3 = NULL,*c4 = NULL;
        while(c1 && c2){
            if(c1->data == c2->data){
                if(c3)c3->next = c1;
                c3 = c1;
                if(c4 == NULL)c4 = c1;
                c1 = c1->next;
                c2 = c2->next;
            }
            else if(c1->data<c2->data){
                c1 = c1->next;
            }
            else{
                c2 = c2->next; 
            }
        }
        if(c3)c3->next = NULL;
        return c4;
    }
};



